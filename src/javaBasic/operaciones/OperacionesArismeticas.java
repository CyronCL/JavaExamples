package javaBasic.operaciones;

public class OperacionesArismeticas {

	public static void main(String[] args) {
		float a = 5;
		float b = 2;
System.out.println(a+b);
System.out.println(a-b);
System.out.println(a%b);
System.out.println(a*b);
System.out.println(a/b);
System.out.println(a++);
System.out.println(a--);

//Para un String el + concadena 
String primero = "El ";
String segundo = "Auto";

System.out.println(primero+segundo);
	}

}
